    package model;

    /**
     *
     * @author Admin
     */
    public class Account {
        int accountId;
        Role role;
        String fullName;
        String email;
        String password;
        String phone;

        public Account() {
        }
  public Account(String fullName, String email, String password, String phone) {
    this.role = new Role(2, "Customer"); // Giả sử roleId = 2 là "Customer"
    this.fullName = fullName;
    this.email = email;
    this.password = password;
    this.phone = phone;
}



        public Account(int accountId, Role role, String fullName, String email, String password, String phone) {
            this.accountId = accountId;
            this.role = role;
            this.fullName = fullName;
            this.email = email;
            this.password = password;
            this.phone = phone;
        }

        public int getAccountId() {
            return accountId;
        }

        public void setAccountId(int accountId) {
            this.accountId = accountId;
        }

        public Role getRole() {
            return role;
        }

        public void setRole(Role role) {
            this.role = role;
        }

        public String getFullName() {
            return fullName;
        }

        public void setFullName(String fullName) {
            this.fullName = fullName;
        }

        public String getEmail() {
            return email;
        }

        public void setEmail(String email) {
            this.email = email;
        }

        public String getPassword() {
            return password;
        }

        public void setPassword(String password) {
            this.password = password;
        }

        public String getPhone() {
            return phone;
        }

        public void setPhone(String phone) {
            this.phone = phone;
        }


    }
